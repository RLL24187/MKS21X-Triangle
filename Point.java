public class Point{
  //Instance variables
  private double x;
  private double y;
  //Constructor for making a point
  public Point(double xcor, double ycor){
    x = xcor;
    y = ycor;
  }
  //Get variables
  public double getX(){
    return this.x;
  }
  public double getY(){
    return this.y;
  }
}
